public class Ceiling {
    private int height;
    private int printedColour;

    public Ceiling(int height, int printedColour) {
        this.height = height;
        this.printedColour = printedColour;
    }

    public int getHeight() {
        return height;
    }

    public int getPrintedColour() {
        return printedColour;
    }
}
